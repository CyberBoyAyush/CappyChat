interface ModelBadgeProps {
  type: "premium" | "super-premium" | "reasoning" | "file-support" | "fast";
  size?: number;
  className?: string;
}

// Icons8 Diamond Icon for Premium
const DiamondIcon: React.FC<{ size?: number; className?: string }> = ({
  size = 22,
  className = "",
}) => <Gem width={size} height={size} className={className} />;

// Icons8 Brain Icon for Reasoning
const BrainIcon: React.FC<{ size?: number; className?: string }> = ({
  size = 26,
  className = "",
}) => <Brain width={size} height={size} className={className} />;

// Icons8 File Icon for File Support
const FileIcon: React.FC<{ size?: number; className?: string }> = ({
  size = 16,
  className = "",
}) => <File width={size} height={size} className={className} />;

// Lightning Icon for Fast Models
const LightningIcon: React.FC<{ size?: number; className?: string }> = ({
  size = 16,
  className = "",
}) => <Zap width={size} height={size} className={className} />;

export const ModelBadge: React.FC<ModelBadgeProps> = ({
  type,
  size = 16,
  className = "",
}) => {
  const badgeStyles = cn(
    "inline-flex items-center gap-1.5 px-0.5 md:px-2 py-1 rounded-md text-xs font-medium transition-colors",
    "text-primary",
    className
  );

  const renderIcon = () => {
    switch (type) {
      case "premium":
        return <DiamondIcon size={size} />;
      case "reasoning":
        return <BrainIcon size={size} />;
      case "file-support":
        return <FileIcon size={size} />;
      case "fast":
        return <LightningIcon size={size} />;
      case "super-premium":
        return (
          <>
            <DiamondIcon size={size} />
            <span className="md:w-1"></span>
            <DiamondIcon size={size} />
          </>
        );
      default:
        return null;
    }
  };

  const renderText = () => {
    switch (type) {
      case "premium":
        return "Premium";
      case "reasoning":
        return "Reasoning";
      case "fast":
        return "Fast";
      default:
        return "";
    }
  };

  return (
    <span className={badgeStyles}>
      {renderIcon()}
      {/* {renderText()} */}
    </span>
  );
};

