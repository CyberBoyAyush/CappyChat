// Model icon selector
export const getModelIcon = (
  provider: string,
  size?: number,
  className?: string
) => {
  const props = { size, className };

  switch (provider.toLowerCase()) {
    case "google":
    case "gemini":
      return <GoogleIcon {...props} />;
    case "openai":
    case "gpt":
      return <OpenAIIcon {...props} />;
    case "anthropic":
    case "claude":
      return <AnthropicIcon {...props} />;
    case "deepseek":
      return <DeepSeekIcon {...props} />;
    case "huggingface":
    case "hf":
      return <HuggingFaceIcon {...props} />;
    case "qwen":
    case "alibaba":
      return <QwenIcon {...props} />;
    case "meta":
      return <MetaIcon {...props} />;
    case "x-ai":
      return <XAIIcon {...props} />;
    case "runware":
      return <RunwareIcon {...props} />;
    default:
      return null;
  }
};
